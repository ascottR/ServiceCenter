// Function to toggle visibility of the customer table
function toggleCustomerTable() {
    const customerTable = document.getElementById('customerTableContainer');
    customerTable.style.display = customerTable.style.display === 'none' ? 'block' : 'none';
  }
  
  // Function to toggle visibility of the booking table
  function toggleBookingTable() {
    const bookingTable = document.getElementById('bookingTableContainer');
    bookingTable.style.display = bookingTable.style.display === 'none' ? 'block' : 'none';
  }
  